def prisma_rect():
    # Programa Calcular el volumen de un prisma rectangular
    print(" Programa 4 Calcular el volumen de un prisma rectangular \n")
    a1 = float(input("Ingrese el largo: "))
    a2 = float(input("Ingrese el ancho: "))
    a3 = float(input("Ingrese la altura: "))
