def sum_numbers():
    # Programa 1 de suma de dos numeros
    print("Programa 1 de suma de dos numeros \n")
    num1 = int(input("Ingrese el primer numero: "))
    num2 = int(input("Ingrese el segundo numero: "))
    suma = num1 + num2
    resta = num1 - num2
    print (f'La suma del numero {num1} + {num2} es {suma}')
    print (f'La resta del numero {num1} - {num2} es {resta}')
    
    input("\n Presiona cualquier tecla para salir del programa: ")
