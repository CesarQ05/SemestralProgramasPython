def area_triangulo():
    #Programa Hallar el area de un triangulo rectangulo
    print("Programa 7 Hallar el area de un triangulo rectangulo \n")
    n1 = float(input("Ingrese la base del triangulo rectangulo: "))
    n2 = float(input("Ingrese la altura del triangulo rectangulo: "))
    area = ((n1 * n2) / 2)
    print (f'El area de un triangulo rectangulo con base = {n1} y altura {n2} es = {area}')
    
    input("\n Presiona cualquier tecla para salir del programa: ")
